// Arithmetic operators

/**
 * '+'    => add
 * '-'    => substract
 * '-expr'=> unary minus, also known as negation
 * '/'    => divide
 * '~/'   => divide and return integer value
 * '*'    => Multiply
 * '%'    => Modulo
 * '++'   => Increment
 * '--'   => Decrement
 */

// Equality and Relational operators

/**
 * >    => Greater than
 * <    => Less than
 * >=   => Greater than or Equal
 * <=   => Less than or Equal
 * ==   => Equality
 * !=   => Not Equal
 */

// Type test operator

/**
 * is   => True if the object has specified type (20 is number) otherwise false
 * is!  => True if the object has not specified type (20 is! String) other wise false
 */

// Bitwise operators
/**
 * AND => a & b
 * OR => a | b
 * XOR => a^b
 * NOT => ~a
 */

// Assignment Operators

/**
 * '='    => Simple Assignment. Assign value from right to left operand
 * '??='  => Assignment if the value is null
 * '+='   => Add and assignment
 * '-='   => Substract and assignment
 * '*='   => Multiply and assignment
 * '/='   => Divide and assignment
 */

// Logical Operators

/**
 * '&&' => True if all specified expression returns true
 * '||' => True if one of all specified expression returns true
 * '!' => Not- inverse the expression results
 */

// Conditional expression

/**
 * 'condition ? 'expr1' : 'expr2' ' => If condition is true, then the expression evaluates expr1 (and returns its value); otherwise, it evaluates and returns the value of expr2.
 * 'expr1 ?? expr2' => If expr1 is non-null, returns its value; otherwise, evaluates and returns the value of expr2
 */

